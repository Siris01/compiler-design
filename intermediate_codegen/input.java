class Main {
	public static void main() {
		int a = 1;
		int b = 2;
		int c = a + b; // Assignment, Declaration, Arithmetic OP
		int result = 0;

		if (c > 5) { // Boolean condition, if-else (Flow of control)
			result = 1;
		} else {
			result = -1;
		}

		int fac = 1;
		for (int i = 1; i <= 10; i++) { // For loop (Flow of control)
			fac = fac * i;
		}
	}
}
